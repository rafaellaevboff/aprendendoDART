void main() {
  double pi = 3.141592;
  int pi_inteiro = pi.toInt(); //numero real pra numero inteiro

  print(pi_inteiro);

  int dezena = 100;
  double dezena_double = dezena.toDouble(); //numero inteirio pra numero real

  print(dezena_double);
}

/*Transformar um valor de um tipo numérico para outro tipo numérico:

nome_da_variável.toDouble();
// Converte para double

nome_da_variável.toInt();
